
def School_age_calculator(Name,age):
    if (age < 5):
        print (Name , ", You are welcome in pre-school" )

    elif (age ==5):
        print (Name, ", You are welcome in kindergarten")

    else :
        print (Name, ", You can join another class")


School_age_calculator("Thomas",3)
